"""
结果解释节点 - 将查询结果转换为自然语言描述
"""
import logging
from typing import Any, Dict, List

from agent.state import AgentState
from config import DEEPSEEK_API_KEY
from services.llm_service import get_llm_service


logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)


def result_interpret_node(state: AgentState) -> Dict[str, Any]:
    """将查询结果转换为自然语言描述。"""
    question = state["question"]
    query_result = state.get("query_result", [])
    generated_sql = state.get("generated_sql", "")
    validation_result = state.get("validation_result", {})

    logger.info(f"[Result Interpret] Interpreting {len(query_result)} query results")

    if not query_result:
        if validation_result.get("valid"):
            final_answer = (
                "查询执行成功，但没有找到匹配的数据。\n\n"
                f"您的问题：{question}\n\n"
                "建议：\n- 检查筛选条件是否过严\n- 尝试缩小时间或范围条件"
            )
        else:
            error_msg = validation_result.get("error", "未知错误")
            final_answer = f"查询失败：{error_msg}\n\n无法为您提供结果，请检查问题或稍后重试。"

        return {
            "final_answer": final_answer,
            "messages": state["messages"],
        }

    llm_answer = try_llm_interpret(question, generated_sql, query_result)
    if llm_answer:
        logger.info("[Result Interpret] Answer generated by LLM")
        return {
            "final_answer": llm_answer,
            "messages": state["messages"],
        }

    result_count = len(query_result)
    result_text = format_results_summary(query_result)
    details_text = build_result_details(query_result, generated_sql, result_count)
    final_answer = f"{result_text}\n\n{details_text}"

    logger.info("[Result Interpret] Answer generated by fallback formatter")
    return {
        "final_answer": final_answer,
        "messages": state["messages"],
    }


def try_llm_interpret(question: str, sql: str, results: List[Dict[str, Any]]) -> str:
    """优先使用 LLM 解释结果，失败时返回空字符串。"""
    if not DEEPSEEK_API_KEY:
        return ""

    try:
        llm = get_llm_service()
        response = llm.interpret_result(question=question, sql=sql, result=results)
        return response.strip()
    except Exception as e:
        logger.warning(f"[Result Interpret] LLM interpretation unavailable: {e}")
        return ""


def format_results_summary(results: List[Dict[str, Any]]) -> str:
    """格式化结果摘要。"""
    if not results:
        return "查询成功，但没有找到匹配的数据。"

    result_count = len(results)
    summary = f"查询成功，找到 **{result_count}** 条记录：\n\n"

    display_count = min(result_count, 5)
    for index, row in enumerate(results[:display_count], 1):
        summary += f"{index}. {format_row_summary(row)}\n"

    if result_count > 5:
        summary += f"\n... 还有 {result_count - 5} 条记录未显示\n"

    return summary


def format_row_summary(row: Dict[str, Any]) -> str:
    """格式化单行结果摘要。"""
    if not row:
        return "空记录"

    values = []
    for _, value in list(row.items())[:3]:
        if isinstance(value, float):
            values.append(f"{value:.2f}")
        elif isinstance(value, int):
            values.append(str(value))
        else:
            str_value = str(value)
            if len(str_value) > 20:
                str_value = str_value[:20] + "..."
            values.append(str_value)

    return " | ".join(values)


def build_result_details(results: List[Dict[str, Any]], sql: str, count: int) -> str:
    """构建结果详情。"""
    details = ["**数据统计**", f"- 总记录数：{count}"]

    if count > 0:
        details.append(f"- 涉及字段：{len(results[0])} 个")

    if sql:
        sql_preview = sql[:100] + ("..." if len(sql) > 100 else "")
        details.append("\n**执行的查询**")
        details.append(f"```sql\n{sql_preview}\n```")

    return "\n".join(details)


def is_successful_result(validation_result: Dict[str, Any]) -> bool:
    """判断是否是成功的查询结果。"""
    if not validation_result:
        return False

    return validation_result.get("valid", False)


def get_result_summary(results: List[Dict[str, Any]]) -> Dict[str, Any]:
    """获取结果摘要信息。"""
    if not results:
        return {
            "count": 0,
            "has_data": False,
            "columns": [],
        }

    return {
        "count": len(results),
        "has_data": True,
        "columns": list(results[0].keys()),
    }
